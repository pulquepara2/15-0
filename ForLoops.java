public class ForLoops {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        System.out.println("-------------------------------");
        for (int j = 1; j <= 30; j++){
            if ( j % 2 == 0){
                System.out.println(j);
            }
        }
    }
}
